/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
    ListNode* getKthFromEnd(ListNode* head, int k) {
        ListNode *dummy = new ListNode(-1);
        dummy->next = head;
        ListNode *p1 = dummy->next, *p2 = dummy->next;
        for(int step = 1;p1 != nullptr && step <= k;step++) {
            p1 = p1->next;
        }

        for(;p1 != nullptr;) {
            p1 = p1->next;
            p2 = p2->next;
        }
        return p2;
    }
};
